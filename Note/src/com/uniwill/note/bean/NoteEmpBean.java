package com.uniwill.note.bean;

public class NoteEmpBean {
	private int empNo;
	private String empNm;
	private long empPay;
	
	public int getEmpNo() {
		return empNo;
	}
	public void setEmpNo(int empNo) {
		this.empNo = empNo;
	}
	public String getEmpNm() {
		return empNm;
	}
	public void setEmpNm(String empNm) {
		this.empNm = empNm;
	}
	public long getEmpPay() {
		return empPay;
	}
	public void setEmpPay(long empPay) {
		this.empPay = empPay;
	}
}
